						<h3>Hoe kan ik updaten van 3.0 naar 3.1?</h3>
						<p>Je kunt van Joomla 3.0 naar 3.1 updaten door middel van één klik in de Joomla!Update component, het is geen migratie en heeft terugwaardse compatibiliteit. Hetzelfde geldt voor iedere voorgaande versie in de Joomla 3 serie. Als je dus nog een Joomla 3.0 site draait, kun je gemakkelijk updaten naar Joomla 3.1 door naar de Joomla!Update component te gaan en daar de update te installeren zodat je gebruik kunt gaan maken van alle nieuwe mogelijkheden en een hoop problemen zijn opgelost.</p>
						<h3>Wat is de status van Joomla 2.5?</h3>
						<p>Versie 2.5 van het Joomla! CMS heeft een langetermijnondersteuning en zal dus nog ondersteund worden tot kort na het verschijnen van Joomla 3.5 die gepland staat voor het voorjaar van 2014. Het is niet direct noodzakelijk om van Joomla 2.5 naar 3.1 te migreren.<strong><br></strong>
						</p>
						<h3>Is het mogelijk om direct te updaten naar Joomla 3 vanuit Joomla 2.5?</h3>
						<p>Overgaan naar Joomla 3.x vanuit Joomla 2.5 is een mini-migratie en geen upgrade, hoewel het migreren van de core van Joomla zelf eenvoudig zou moeten zijn. Het is wel mogelijk dat de templates voor Joomla 2.5 aangepast moeten worden om met Joomla 3 te werken en hetzelfde geldt voor veel extensies. Test dus altijd eerst voordat je migreert en controleer de ontwikkeling van extensies en templates die je gebruikt.</p>
						<h3>Wat is de status van Joomla 1.5?</h3>
						<p>Joomla 1.5 werd officieel niet meer ondersteund vanaf april 2012 maar de ondersteuning is nog tot eind 2012 onofficieel voortgezet voor veiligheidsproblemen van gemiddelde en hoge prioriteit.</p>
						<p>Betekent dat dat je 1.5 website ineens niet meer werkt? Nee, je website blijft gewoon draaien zoals het altijd heeft gedaan. Bedenk wel dat Joomla ontwikkelaars geen nieuwe versies van hun extensies voor Joomla 1.5 zullen ontwikkelen en vrijgeven, dus problemen en vooral ook veiligheidsproblemen worden niet verholpen. Het is daarom beter om te migreren naar een hogere versie.</p>
						<p>Overgaan van 2.5 naar een van de Joomla 3 versies is relatief eenvoudig sinds het proces gemakkelijker is gemaakt voor nieuwe versies. Helaas gaat dat niet op voor de migratie vanuit 1.5. Gelukkig zijn er twee goede extensies die je bij het proces helpen:
							<a target="_blank" href="http://extensions.joomla.org/extensions/migration-a-conversion/joomla-migration/11658">jUpgrade</a> and
							<a target="_blank" href="http://extensions.joomla.org/extensions/migration-a-conversion/joomla-migration/15609">SPUpgrade</a>.
						</p>
						<p>Je kunt kiezen of je direct naar joomla 3.0 wilt migreren of eerst een tussenstap naar 2.5 wilt maken. Beide componenten bieden je de keuze. Bestudeer de documentatie van de componenten om te leren hoe je kunt migreren van Joomla 1.5 to 3.0/2.5.</p>
						<p>Voor de meeste nieuwe/gemigreerde websites is Joomla 3 de aanbevolen versie. Door deze versie te gebruiken voorkom je een mini-migratie en terugwaardse compatibiliteit is langer gegarandeerd. De ondersteuning van 3.x komt pas in 2016 ten einde.</p>
